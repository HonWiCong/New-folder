import axios from 'axios';

import { ITOrgContactPerson } from '@/shared/model/t-org-contact-person.model';

const baseApiUrl = 'api/t-org-contact-people';

export default class TOrgContactPersonService {
  public find(id: number): Promise<ITOrgContactPerson> {
    return new Promise<ITOrgContactPerson>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: ITOrgContactPerson): Promise<ITOrgContactPerson> {
    return new Promise<ITOrgContactPerson>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: ITOrgContactPerson): Promise<ITOrgContactPerson> {
    return new Promise<ITOrgContactPerson>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: ITOrgContactPerson): Promise<ITOrgContactPerson> {
    return new Promise<ITOrgContactPerson>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
