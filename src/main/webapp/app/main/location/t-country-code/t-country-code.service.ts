import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { ITCountryCode } from '@/shared/model/t-country-code.model';

const baseApiUrl = 'api/t-country-codes';

export default class TCountryCodeService {
	public find(id: number): Promise<ITCountryCode> {
		return new Promise<ITCountryCode>((resolve, reject) => {
			axios
				.get(`${baseApiUrl}/${id}`)
				.then(res => {
					resolve(res.data);
				})
				.catch(err => {
					reject(err);
				});
		});
	}

	public retrieve(paginationQuery?: string): Promise<any> {
		return new Promise<any>((resolve, reject) => {
			axios
				.get(baseApiUrl + `?${paginationQuery}`)
				.then(res => {
					resolve(res);
				})
				.catch(err => {
					reject(err);
				});
		});
	}

	public delete(id: number): Promise<any> {
		return new Promise<any>((resolve, reject) => {
			axios
				.delete(`${baseApiUrl}/${id}`)
				.then(res => {
					resolve(res);
				})
				.catch(err => {
					reject(err);
				});
		});
	}

	public create(entity: ITCountryCode): Promise<ITCountryCode> {
		return new Promise<ITCountryCode>((resolve, reject) => {
			axios
				.post(`${baseApiUrl}`, entity)
				.then(res => {
					resolve(res.data);
				})
				.catch(err => {
					reject(err);
				});
		});
	}

	public update(entity: ITCountryCode): Promise<ITCountryCode> {
		return new Promise<ITCountryCode>((resolve, reject) => {
			axios
				.put(`${baseApiUrl}/${entity.id}`, entity)
				.then(res => {
					resolve(res.data);
				})
				.catch(err => {
					reject(err);
				});
		});
	}

	public partialUpdate(entity: ITCountryCode): Promise<ITCountryCode> {
		return new Promise<ITCountryCode>((resolve, reject) => {
			axios
				.patch(`${baseApiUrl}/${entity.id}`, entity)
				.then(res => {
					resolve(res.data);
				})
				.catch(err => {
					reject(err);
				});
		});
	}
}
